function Word(word, number) {
	this.word   = word;
	this.number = number;

	var dieFaces = {
		"1": "&#9856;",
		"2": "&#9857;",
		"3": "&#9858;",
		"4": "&#9859;",
		"5": "&#9860;",
		"6": "&#9861;"
	}

	this.hexColor = function() {
		hexColor = this.number.toString(16);
		while (hexColor.length < 6) {
			hexColor = "8" + hexColor;
		}
		return hexColor;
	}

	this.dice = function() {
		dice = '';
		$.each(this.number.split(''), function(k, v) {
			dice = dice + " " + dieFaces[v];
		});
		return dice.trim();
	}
}

window.Seeder = {
	seedLimit: 300,
	seeds: 0,

	push: function(seed) {
		Math.seedrandom(seed, true);
		this.seeds++;
	},

	isDone: function() {
		if (this.seeds == this.seedLimit) {
			return true;
		}
		return false;
	},

	percentage: function() {
		return Math.round((this.seeds / this.seedLimit) * 100)
	}
}

